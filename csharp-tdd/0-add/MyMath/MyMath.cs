namespace MyMath;
    
    /// <summary>
    /// Mathematically adds two integers.
    /// </summary>
public class Operations
{
  /// <summary>
  /// Adds two integers.
  /// </summary>
  /// <param name="a">The first integer.</param>
  /// <param name="b">The second integer.</param>
  /// <returns>The sum of the two integers, a and b.</returns>
  public static int Add(int a, int b)
  {
    return a + b;
  }
}
